export const ImageUpload = () => {
  const classes = useStyles();
  const [selectedFile, setSelectedFile] = useState();
  const [preview, setPreview] = useState();
  const [data, setData] = useState();
  const [image, setImage] = useState(false);
  const [isLoading, setIsloading] = useState(false);

  const [medicine, setMedicine] = useState("");


  const MEDICINE_INFO = {
    'Healthy_Leaf': "No treatment needed. Maintain healthy practices.",
    'Healthy_Nut': "No treatment needed. Maintain healthy practices.",
    'Healthy_Trunk': "No treatment needed. Maintain healthy practices.",
    'Mahali_Koleroga': "Spray Bordeaux mixture (1%) or Copper Oxychloride (3 g/L).",
    'Stem_bleeding': "Apply Tridemorph (0.1%) or Copper Oxychloride (5 g/L).",
    'bud_borer': "Spray Chlorpyrifos (0.05%) or Neem Oil (5 ml/L).",
    'healthy_foot': "No treatment needed. Maintain healthy practices.",
    'stem cracking': "Apply Copper Oxychloride (0.3%) and avoid waterlogging.",
    'yellow leaf disease': "Spray Fosetyl-Al (0.2%) or Potassium Phosphonate (0.3%).",
  }



  let confidence = 0;


  const sendFile = async () => {
    if (image) {
      let formData = new FormData();
      formData.append("file", selectedFile);
      let res = await axios({
        method: "post",
        url: 'http://localhost:8080/predict',
        data: formData,
      });
      if (res.status === 200) {
        setData(res.data);
        // console.log(res.data.class);
        if (res.data.class === 'Healthy_Leaf') {
          setMedicine(MEDICINE_INFO['Healthy_Leaf']);
        } else if (res.data.class === 'Healthy_Nut') {
          setMedicine(MEDICINE_INFO['Healthy_Nut']);
        } else if (res.data.class === 'Healthy_Trunk') {
          setMedicine(MEDICINE_INFO['Healthy_Trunk']);
        } else if (res.data.class === 'Mahali_Koleroga') {
          setMedicine(MEDICINE_INFO['Mahali_Koleroga']);
        } else if (res.data.class === 'Stem_bleeding') {
          setMedicine(MEDICINE_INFO['Stem_bleeding']);
        } else if (res.data.class === 'bud_borer') {
          setMedicine(MEDICINE_INFO['bud_borer']);
        } else if (res.data.class === 'healthy_foot') {
          setMedicine(MEDICINE_INFO['healthy_foot']);
        } else if (res.data.class === 'stem cracking') {
          setMedicine(MEDICINE_INFO['stem cracking']);
        } else if (res.data.class === 'yellow leaf disease') {
          setMedicine(MEDICINE_INFO['yellow leaf disease']);
        } else {
          setMedicine("No treatment needed. Maintain healthy practices.");
        }
      }
      setIsloading(false);
    }
  }

  const clearData = () => {
    setData(null);
    setImage(false);
    setSelectedFile(null);
    setPreview(null);
    setMedicine("");
  };

  useEffect(() => {
    if (!selectedFile) {
      setPreview(undefined);
      return;
    }
    const objectUrl = URL.createObjectURL(selectedFile);
    setPreview(objectUrl);
  }, [selectedFile]);

  useEffect(() => {
    if (!preview) {
      return;
    }
    setIsloading(true);
    sendFile();
  }, [preview]);

  const onSelectFile = (files) => {
    if (!files || files.length === 0) {
      setSelectedFile(undefined);
      setImage(false);
      setData(undefined);
      return;
    }
    setSelectedFile(files[0]);
    setData(undefined);
    setImage(true);
  };

  if (data) {
    confidence = (parseFloat(data.confidence) * 100).toFixed(2);
  }



  return (
    <React.Fragment>
      <Navbar />
      <Container maxWidth={false} className={classes.mainContainer} disableGutters={true}>
        <Grid
          className={classes.gridContainer}
          container
          direction="row"
          justifyContent="center"
          alignItems="center"
          spacing={2}
        >
          <Grid item xs={12}>
            <Card className={`${classes.imageCard} ${!image ? classes.imageCardEmpty : ''}`}>
              {image && <CardActionArea>
                <CardMedia
                  className={classes.media}
                  image={preview}
                  component="image"
                  title="Contemplative Reptile"
                />
              </CardActionArea>
              }
              {!image && <CardContent className={classes.content}>
                <DropzoneArea
                  acceptedFiles={['image/*']}
                  dropzoneText={"Drag and drop an image of a potato plant leaf to process"}
                  onChange={onSelectFile}
                />
              </CardContent>}
              {data && <CardContent className={classes.detail}>
                <TableContainer component={Paper} className={classes.tableContainer}>
                  <Table className={classes.table} size="small" aria-label="simple table">
                    <TableHead className={classes.tableHead}>
                      <TableRow className={classes.tableRow}>
                        <TableCell className={classes.tableCell1}>Label:</TableCell>
                        <TableCell align="right" className={classes.tableCell1}>Confidence:</TableCell>
                      </TableRow>
                    </TableHead>
                    <TableBody className={classes.tableBody}>
                      <TableRow className={classes.tableRow}>
                        <TableCell component="th" scope="row" className={classes.tableCell}>
                          {data.class}

                        </TableCell>
                        <TableCell align="right" className={classes.tableCell}>{confidence}%</TableCell>
                      </TableRow>
                    </TableBody>
                  </Table>
                </TableContainer>
              </CardContent>}
              {isLoading && <CardContent className={classes.detail}>
                <CircularProgress color="secondary" className={classes.loader} />
                <Typography className={classes.title} variant="h6" noWrap>
                  Processing
                </Typography>
              </CardContent>}
            </Card>
          </Grid>
          {data &&
            <Grid item className={classes.buttonGrid} >

              <ColorButton variant="contained" className={classes.clearButton} color="primary" component="span" size="large" onClick={clearData} startIcon={<Clear fontSize="large" />}>
                Clear
              </ColorButton>
            </Grid>}
        </Grid >
      </Container >
      <div className="medicines">
        <div className="card">
          <svg className="wave" viewBox="0 0 1440 320" xmlns="http://www.w3.org/2000/svg">
            <path d="M0,256L11.4,240C22.9,224,46,192,69,192C91.4,192,114,224,137,234.7C160,245,183,235,206,213.3C228.6,192,251,160,274,149.3C297.1,139,320,149,343,181.3C365.7,213,389,267,411,282.7C434.3,299,457,277,480,250.7C502.9,224,526,192,549,181.3C571.4,171,594,181,617,208C640,235,663,277,686,256C708.6,235,731,149,754,122.7C777.1,96,800,128,823,165.3C845.7,203,869,245,891,224C914.3,203,937,117,960,112C982.9,107,1006,181,1029,197.3C1051.4,213,1074,171,1097,144C1120,117,1143,107,1166,133.3C1188.6,160,1211,224,1234,218.7C1257.1,213,1280,139,1303,133.3C1325.7,128,1349,192,1371,192C1394.3,192,1417,128,1429,96L1440,64L1440,320L1428.6,320C1417.1,320,1394,320,1371,320C1348.6,320,1326,320,1303,320C1280,320,1257,320,1234,320C1211.4,320,1189,320,1166,320C1142.9,320,1120,320,1097,320C1074.3,320,1051,320,1029,320C1005.7,320,983,320,960,320C937.1,320,914,320,891,320C868.6,320,846,320,823,320C800,320,777,320,754,320C731.4,320,709,320,686,320C662.9,320,640,320,617,320C594.3,320,571,320,549,320C525.7,320,503,320,480,320C457.1,320,434,320,411,320C388.6,320,366,320,343,320C320,320,297,320,274,320C251.4,320,229,320,206,320C182.9,320,160,320,137,320C114.3,320,91,320,69,320C45.7,320,23,320,11,320L0,320Z" fillOpacity={1} />
          </svg>
          {/* <div className="icon-container">
            <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 512 512" strokeWidth={0} fill="currentColor" stroke="currentColor" className="icon">
              <path d="M256 48a208 208 0 1 1 0 416 208 208 0 1 1 0-416zm0 464A256 256 0 1 0 256 0a256 256 0 1 0 0 512zM369 209c9.4-9.4 9.4-24.6 0-33.9s-24.6-9.4-33.9 0l-111 111-47-47c-9.4-9.4-24.6-9.4-33.9 0s-9.4 24.6 0 33.9l64 64c9.4 9.4 24.6 9.4 33.9 0L369 209z" />
            </svg>
          </div> */}
          <div className="message-text-container">
            <p className="message-text">Medicine</p>
            <p className="sub-text">{medicine}</p>
          </div>
        </div>
      </div>
      <Footer />
    </React.Fragment >
  );
};
